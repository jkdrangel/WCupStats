package model;

import java.util.Date;

/**
 *
 * @author lsantana
 */
public class Copa {
    
    private Date data;
    private int ID;

    public Copa(Date data) {
        this.data = data;
    }

    public Date getData() {
        return data;
    }

    public void setData(Date data) {
        this.data = data;
    }

    public int getID() {
        return ID;
    }

    public void setID(int ID) {
        this.ID = ID;
    }

    @Override
    public String toString() {
        return "Copa{" + "data=" + data + '}';
    }
    
    
}
